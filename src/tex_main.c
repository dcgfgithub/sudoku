/*
** EXPORTED VARIABLES
*/
SWtexture tex_main_texture;
SWimage   tex_main_images[ 36 ];

/*
** FUNCTIONs
*/
void tex_main_LoadImages( void )
{
    swTexGen ( 1, &tex_main_texture );
    swTexLoad( tex_main_texture, "tex_main.png" );

    swImgGen( 36, tex_main_images);
    swImgMapFrames( 36, tex_main_images, tex_main_texture, 36, tex_main_img_frames );
}

void tex_main_UnloadImages( void )
{
    swImgDel( 36, tex_main_images);
    swTexDel( 1, &tex_main_texture );
}

